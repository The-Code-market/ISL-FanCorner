package com.codemaster.fancorner.model;

public class UserDetails {
    public String uid;
    public String userName;
    public String team;
    public String phone;

    public UserDetails(String uid, String userName, String team, String phone) {
        this.uid = uid;
        this.userName = userName;
        this.team = team;
        this.phone = phone;
    }
}
